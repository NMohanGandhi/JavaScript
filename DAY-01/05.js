/*
5.Create a function called sumOfSquares that takes 
two numbers as parameters and returns the sum of 
their squares. For example, sumOfSquares(3, 4) 
should return 25 (which is 3 squared plus 4 squared).
*/

function sumOfSquares(a,b){
    return(
        (a*a)+(b*b)
    )
}
console.log(sumOfSquares(3,4));
